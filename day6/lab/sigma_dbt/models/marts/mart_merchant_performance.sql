WITH filtered_transactions AS (
    SELECT
        transaction_id,
        amount,
        status,
        merchant_id,
        customer_id,
        transaction_date,
        payment_method
    FROM {{ ref('fact_transactions') }}
    WHERE status IN ('COMPLETED', 'FAILED')
),

merchant_details AS (
    SELECT
        merchant_id,
        merchant_name,
        category,
        city,
        onboarded_date
    FROM {{ ref('dim_merchant') }}
),

aggregated_metrics AS (
    SELECT
        ft.merchant_id,
        COUNT(ft.transaction_id) AS total_transactions,
        COUNT(CASE WHEN ft.status = 'FAILED' THEN 1 END) AS failed_count,
        SUM(CASE WHEN ft.status = 'COMPLETED' THEN ft.amount ELSE 0 END) AS total_revenue,
        COUNT(DISTINCT ft.customer_id) AS unique_customers
    FROM filtered_transactions ft
    GROUP BY ft.merchant_id
),

calculated_metrics AS (
    SELECT
        am.merchant_id,
        am.total_transactions,
        am.failed_count,
        am.total_revenue,
        am.unique_customers,
        (am.failed_count::FLOAT / am.total_transactions) * 100 AS failure_rate_pct,
        (am.total_revenue / NULLIF(am.total_transactions, 0)) AS avg_transaction_value
    FROM aggregated_metrics am
)

SELECT
    md.merchant_id,
    md.merchant_name,
    md.category,
    md.city,
    md.onboarded_date,
    cm.total_transactions,
    cm.failed_count,
    cm.failure_rate_pct,
    cm.total_revenue,
    cm.avg_transaction_value,
    cm.unique_customers
FROM calculated_metrics cm
JOIN merchant_details md ON cm.merchant_id = md.merchant_id
